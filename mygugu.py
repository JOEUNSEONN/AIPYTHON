# 두 개의 숫자를 입력받아 사칙연산을 수행하는 프로그램

# 사용자로부터 두 개의 숫자를 입력받습니다.
x = int(input("첫 번째 숫자를 입력하세요: "))
y = int(input("두 번째 숫자를 입력하세요: "))

# 사칙연산 결과 출력
print(f"{x} + {y} = {x + y}")
print(f"{x} - {y} = {x - y}")
print(f"{x} × {y} = {x * y}")

# 나눗셈 예외 처리 (0으로 나누는 경우)
if y != 0:
    print(f"{x} ÷ {y} = {x / y:.2f}")  # 소수점 두 자리 출력
else:
    print("0으로 나눌 수 없습니다.")
